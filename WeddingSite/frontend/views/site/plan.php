                    <h4><u>Procedure for obtaining Indian visa</u></h4>
                    Cost: roughly $150 <br />
                    Estimated Time: 10 days <br />
                    (<u>Other Options</u> - If you don’t want to have to pay $150 for a visa, i.e. - if this is likely the only time 
                    you will ever be going to India, you may want to check out the e-visa option. It can be made 120 days/4 months 
                    before your date of arrival, is good for 60 days from your date of arrival, and you can get it twice a year. 
                    It is also half the cost at $75. If this is the option you’re choosing, read no further, wait until around October 
                    and November, and get your visa then. However, since that is not the process I went through, I will be no help 
                    whatsoever on that one. If you think you might be going to India more than just once in the next decade, probably 
                    cheaper and less hassle all round to go through the process detailed below:)
                    <h4><u>What you’ll need:</u></h4>
                    <ul>
                        <li>A valid passport</li>
                        <li>Recent passport-sized (2x2) photos that aren’t the same as the photo in your passport (You can get these 
                            at Walgreens or Fed-Ex for about $15, or you can just make your own if you are handy enough on the computer:)</li>
                        <li>Glue/glue stick (no - you are not allowed to use tape, staples, etc. Glue is specified...that’s bureaucracy folks;)</li>
                        <li>1 UPS Express Envelope (they are the red and brown envelopes and can be picked up at Staples for free)</li>
                        <li>Copy of proof of address (driver’s license, utility bill, etc.)</li>
                        <li>Printer for printing off everything you’ll need to send in (if you don’t have one, you are sent an email with 
                            a link so you can pull up all the paperwork and print it out elsewhere)</li>
                    </ul>
                    <h4><u>Step 1:</u></h4>
                    <ul>
                        <li>Go to <a href=https://www.in.ckgs.us/visa">https://www.in.ckgs.us/visa</a> </li>
                        <li>On this page, you should see the “Visa Application - Get Started” section with all the questions/yes-no answers. 
                            If you don’t see this page, go to almost the top of the page.</li>
                        <li>Right next to the HOME icon is the visa button. Hover over that, and click on the “Visa Application - Get Started” 
                            link. It’s pretty straightforward. <i>This 3rd party deals only in Tourist Visas that are good for 10 years and allow 
                                multiple entries (this is important down the road)</i></li>
                    </ul>
                    <h4><u>Step 2:</u></h4>
                    <ul>
                        <li>
                        Once you are finished filling out this starting form and have hit “Proceed”, you should be given a reference/temp. ID number 
                    and re-directed to the government of India website. Keep track of the number in case you need to take a break and come back 
                    later. Also, once they have your email and phone number, they will send you frequent updates on where you’re at and what your 
                    visa status is. Once on Indian government website, click on the “<b>REGULAR VISA APPLICATION</b>” tab at top (the orange one).
                        </li>
                    </ul>
                    <h4><u>Step 3:</u></h4>
                    <ul>
                        <li>
                        Fill out the online form. If you need to take a break, save what you have, and you will be given a different reference 
                        number so you can come back to it later. You need to be able to upload one of the passport photos to the application. 
                        If the file size is too large, you should be able to scale it down to size. **Important: When you get to the question on 
                        the application that asks your number of entries and the period of your visa in months, answer respectively “Multiple” 
                        and “120”. It is not asking about just this particular trip - it is asking for the type of visa you are seeking which is 
                        the 10-yr./120 mo., multiple entry visa** Towards the end when it asks for a reference in India, unless you already have 
                        a contact, use: <br /><br />
                        Dennis Ksing <br />
                        #173 Suvidha Apts, GH 10, Sector 56 <br />
                        Gurgaon, Haryana <br />
                        Phone #: 919871997366 <br />
                        </li>
                    </ul>
                    <h4><u>Step 4:</u></h4>
                    <ul>
                        <li>
                            When you are finished filling out the form, either print it out or wait until later. Next to the <b>PRINT FORM</b> button is a 
                            <b>BOOK APPOINTMENT</b> button. Click it - this will take you back to the CKGS website.
                        </li>
                    </ul>
                    <h4><u>Step 5:</u></h4>
                    <ul>
                        <li>
                            Once you are back on the CKGS website, you should be taken to the next step. You need to fill out 2 more forms: the 
                            Additional Particulars Form and the Religious Undertaking Letter (under Church Affiliation name, I just put Orthodox 
                            Presbyterian Church).
                        </li>
                    </ul>
                    <h4><u>Step 6:</u></h4>
                    <ul>
                        <li>
                            Once you are done with those two forms, it will lead you through the payment/shipping options. I just used UPS for 
                            sending and receiving. It’s $15 each way, and as long as you have the return label/shipping paid for, you don’t have 
                            to worry about sending them another pre-paid envelope in which to return your passport. Once your payment has gone 
                            through, they will send you an email receipt with links to all the paperwork that you need to print out.
                        </li>
                    </ul>
                    <h4><u>Step 7:</u> Assembling everything (YAY!!!).</h4> 
                    <ol>
                        <li>
                            OK, first thing to print out (you may already have done this) is the all-important checklist. There should be a 
                            link to this in the final email you received upon completion/payment. Remember to check the items off as you go.
                        </li>
                        <li>
                            Second is the Visa Application that you filled out on the Indian Government website. If you didn’t print it out 
                            before you returned to CKGS, you can use your ID# to get back on and print it out. There should be 2 pages to the 
                            printed version, and your uploaded photo should show in the top LH corner. There is a space in the top RH corner for 
                            you to paste (here comes the gluestick:) the hard copy of your photo. You also need to sign underneath the photo you 
                            just pasted on, and you need to sign at the bottom of the second page. Check that off the checklist.
                        </li>
                        <li>
                            Third, you need your original passport plus a copy of the two pages in your passport that have your picture, 
                            signature, and info. Check that off your list. (Zooming right through!)
                        </li>
                        <li>
                            Next is a copy (not original) of either your driver’s license or a utility bill. Check.
                        </li>
                        <li>
                            After that comes another photograph - yes, this is a separate hard copy photo from the one you glued to the front 
                            (same image, but just paperclipped to the front of your visa application. Note: passport photos come in 2s, so you’ll 
                            have plenty of photos.) Check.
                        </li>
                        <li>
                            Go ahead and print out that Additional Particulars Form and sign it at the bottom. Add it to your pile and 
                            check off that box.
                        </li>
                        <li>
                            Print out a copy of your online payment receipt from CKGS. Check.
                        </li>
                        <li>
                            **On the checklist, do not worry about item 8. Since you already paid CKGS for the return shipping, you do not need 
                            to worry about having a prepaid envelope included with your materials. <u><i>But still check that box off!</i></u>**
                        </li>
                        <li>
                            Last, print out the religious undertaking letter and sign it. Check. (You’re almost there!!!)(Note: anything that 
                            is more than one page may be stapled - just don’t staple everything together in one big lump.)
                        </li>
                    </ol>
                    <h4><u>Step 8:</u></h4>
                    <ul>
                        <li>
                            Now that you have everything assembled, make sure it’s in the correct order - go through it with the checklist one more time. 
                            Don’t forget: the checked-off checklist needs to go with your stuff on the top of the pile. Put everything into your UPS Express 
                            Envelope and seal it up. If you haven’t already printed out your shipping label, do so now and put it on your package in the 
                            appropriate place (it’s marked where it goes). You also need to print out the barcode you received and place that 
                            <i><u>underneath</u></i> the shipping label.
                        </li>
                    </ul>
                    <h4><u>Step 9:</u></h4>
                    <ul>
                        <li>
                            Drop off your envelope at the nearest Staples or UPS store and do a jig on your way back out:)
                        </li>
                    </ul>
                    <p>
                        If everything goes smoothly, you should receive your passport/Indian visa back within about 10 days. At every step of the way, CKGS 
                        emails and texts you to let you know the status of your visa and where your passport is in transit to or from. There doesn’t seem to 
                        be any rhyme or reason to where they place the visa in your passport - just keep looking until you see it:)
                    </p>
                    <p>
                        If you have any questions, concerns, or you want to organize an India visa party night (why not?), just let me know. 
